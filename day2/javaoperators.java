package day2;
import java.util.*;
public class javaoperators {
    public static void main (String arg[]){
        Scanner sc = new Scanner (System.in);
        //Operators
        //arithmetic unary 
        //increment ++
        //post increment
        int a = 10;
        int b= a++;
        System.out.println(b);
        System.out.println(a);
        //pre increment
        int c= 10;
        int d= ++c;
        System.out.println(c);
        System.out.println(d);

    }
    
}

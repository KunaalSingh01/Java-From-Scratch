public class BinaryOpr{
    public static void main(String[] args) {
        System.out.println(5&4); // Binary AND 
        System.out.println(5|4); // Binary OR
        System.out.println(5^4); // Binary XOR
        System.out.println(~7);  // Binary One's Compliment (NOT).
        System.out.println(8<<3); // Binary LeftShift (a<<b = a*2^b) 
        System.out.println(7>>2); // Binary RightShift (a>>b = b/2^b)

    }
}